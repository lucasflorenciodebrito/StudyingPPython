"""
Faça um programa que receba uma string e responda se eça tem uma vogal,
se sim, quais são? 

"""

mensagem = input("Digite o texto: ").lower()
if 'a' in mensagem:
	print("O texto contem a vogal a")
if 'e' in mensagem:
	print("O texto contem a vogal e")
if 'i' in mensagem:
	print ("O texto contem a vogal i")
if 'o' in mensagem:
	print("O texto contem a vogal o")
if 'u' in mensagem:
	print("O texto contem a vogal u")